type testHook struct {
	hasError bool
}

func (t *testHook) Levels() []logrus.Level {
	return []logrus.Level{logrus.ErrorLevel}
}

func (t *testHook) Fire(entry *logrus.Entry) error {
	if entry.Level == logrus.ErrorLevel {
		t.hasError = true
	}
	return nil
}

